package com.navercorp.test.pinpoint.testweb.controller;

import io.lettuce.core.RedisClient;
import io.lettuce.core.api.StatefulRedisConnection;
import io.lettuce.core.api.sync.RedisCommands;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.ResponseBody;

import javax.servlet.http.HttpServletRequest;

@Controller
public class LettuceController {
    private static final String HOST = "10.113.160.166";
    private static final int PORT = 6390;

    private final Logger logger = LoggerFactory.getLogger(this.getClass());

    @RequestMapping(value = "/lettuce/get")
    @ResponseBody
    public String get(HttpServletRequest request) {
        RedisClient redisClient = RedisClient.create("redis://" + HOST + ":" + PORT);
        StatefulRedisConnection<String, String> connection = redisClient.connect();
        RedisCommands<String, String> syncCommands = connection.sync();

        syncCommands.set("key", "Hello, Redis!");

        connection.close();
        redisClient.shutdown();
        return "OK";
    }
}
